#include<stdio.h>
int main()
{
    int num1,num2,result;
    num1=20;
    num2=30;
result=num1+num2;
    printf("%d+%d=%d",num1,num2,result);
    printf("\n");
result=num1-num2;
printf("%d-%d=%d",num1,num2,result);
printf("\n");
result=num1*num2;
printf("%d*%d=%d",num1,num2,result);
printf("\n");
result=num1/num2;
printf("%d/*%d=%d",num1,num2,result);
printf("\n");
}
